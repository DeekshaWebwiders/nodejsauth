import { toast } from "react-toastify";

// âœ… Success Toast
export const showSuccessToast = (message = "Success") => {
  toast.success(message, {
    position: "top-right",
    autoClose: 3000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
  });
};

